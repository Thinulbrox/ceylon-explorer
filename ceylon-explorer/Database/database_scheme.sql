CREATE DATABASE IF NOT EXISTS ceylon_tourism CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE ceylon_tourism;
CREATE TABLE IF NOT EXISTS attractions (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    description TEXT,
    location VARCHAR(255),
    region VARCHAR(100),
    province VARCHAR(100),
    category VARCHAR(100),
    rating DECIMAL(3, 2) DEFAULT 0.0,
    entrance_fee VARCHAR(100),
    opening_hours VARCHAR(255),
    best_time_to_visit VARCHAR(255),
    coordinates VARCHAR(100),
    image_url TEXT,
    website VARCHAR(255),
    distance_from_colombo INT,
    facilities TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS hotels (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    location VARCHAR(255),
    region VARCHAR(100),
    province VARCHAR(100),
    accommodation_type VARCHAR(100),
    rating DECIMAL(3, 2) DEFAULT 0.0,
    price_range VARCHAR(100),
    amenities TEXT,
    contact VARCHAR(255),
    website VARCHAR(255),
    description TEXT,
    room_count INT,
    check_in_time VARCHAR(50),
    check_out_time VARCHAR(50),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS restaurants (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    location VARCHAR(255),
    region VARCHAR(100),
    cuisine VARCHAR(100),
    rating DECIMAL(3, 2) DEFAULT 0.0,
    price_range VARCHAR(100),
    specialties TEXT,
    contact VARCHAR(255),
    opening_hours VARCHAR(255),
    description TEXT,
    seating_capacity INT,
    delivery_available BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS beaches (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    location VARCHAR(255),
    region VARCHAR(100),
    province VARCHAR(100),
    description TEXT,
    rating DECIMAL(3, 2) DEFAULT 0.0,
    activities TEXT,
    best_time_to_visit VARCHAR(255),
    facilities TEXT,
    safety_info TEXT,
    water_sports_available BOOLEAN DEFAULT FALSE,
    lifeguard_available BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS cultural_sites (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    location VARCHAR(255),
    region VARCHAR(100),
    province VARCHAR(100),
    type VARCHAR(100),
    description TEXT,
    rating DECIMAL(3, 2) DEFAULT 0.0,
    historical_period VARCHAR(255),
    entrance_fee VARCHAR(100),
    opening_hours VARCHAR(255),
    significance TEXT,
    unesco_site BOOLEAN DEFAULT FALSE,
    dress_code TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS wildlife_parks (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    location VARCHAR(255),
    region VARCHAR(100),
    province VARCHAR(100),
    type VARCHAR(100),
    description TEXT,
    rating DECIMAL(3, 2) DEFAULT 0.0,
    animals_seen TEXT,
    best_time_to_visit VARCHAR(255),
    entrance_fee VARCHAR(100),
    safari_options TEXT,
    area_sqkm DECIMAL(8, 2),
    established_year INT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS tea_estates (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    location VARCHAR(255),
    region VARCHAR(100),
    province VARCHAR(100),
    description TEXT,
    rating DECIMAL(3, 2) DEFAULT 0.0,
    tea_types TEXT,
    tour_available BOOLEAN DEFAULT TRUE,
    tasting_available BOOLEAN DEFAULT TRUE,
    entrance_fee VARCHAR(100),
    opening_hours VARCHAR(255),
    established_year INT,
    altitude_m INT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS activities (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    location VARCHAR(255),
    region VARCHAR(100),
    category VARCHAR(100),
    description TEXT,
    rating DECIMAL(3, 2) DEFAULT 0.0,
    price_range VARCHAR(100),
    duration VARCHAR(100),
    difficulty_level VARCHAR(50),
    best_time VARCHAR(255),
    equipment_provided BOOLEAN DEFAULT FALSE,
    booking_required BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS transport (
    id INT PRIMARY KEY AUTO_INCREMENT,
    type VARCHAR(100) NOT NULL,
    route VARCHAR(255),
    operator VARCHAR(255),
    description TEXT,
    price_range VARCHAR(100),
    schedule_info TEXT,
    booking_info VARCHAR(255),
    contact VARCHAR(255),
    amenities TEXT,
    duration VARCHAR(100),
    frequency VARCHAR(100),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS events (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    location VARCHAR(255),
    region VARCHAR(100),
    type VARCHAR(100),
    description TEXT,
    date_period VARCHAR(255),
    significance TEXT,
    activities TEXT,
    visitor_info TEXT,
    entry_fee VARCHAR(100),
    annual_event BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
