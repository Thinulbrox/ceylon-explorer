CREATE VIEW top_attractions AS
SELECT name,
    location,
    region,
    category,
    rating,
    description
FROM attractions
WHERE rating >= 4.5
ORDER BY rating DESC;
CREATE VIEW beach_destinations AS
SELECT b.name,
    b.location,
    b.region,
    b.rating,
    b.activities,
    h.name as nearby_hotel,
    h.rating as hotel_rating
FROM beaches b
    LEFT JOIN hotels h ON b.region = h.region
WHERE b.rating >= 4.0;
CREATE VIEW heritage_sites AS
SELECT name,
    location,
    type,
    historical_period,
    significance,
    entrance_fee,
    rating,
    unesco_site
FROM cultural_sites
ORDER BY rating DESC;
